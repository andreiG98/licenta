import torch
import torch.nn as nn
import torchvision.models as models

class SEModule(nn.Module):

    def __init__(self, channels, reduction):
        super(SELayer, self).__init__()
        
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        mid_channels = channels // reduction
        # self.fc1 = nn.Conv2d(channels, channels // reduction, kernel_size=1,
        #                      padding=0)
        self.fc1 = conv1x1(channels, mid_channels)
        self.activ = nn.ReLU(inplace=True)
        # self.fc2 = nn.Conv2d(channels // reduction, channels, kernel_size=1,
        #                      padding=0)
        self.fc2 = conv1x1(mid_channels, channels)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        module_input = x
        x = self.avg_pool(x)
        x = self.fc1(x)
        x = self.activ(x)
        x = self.fc2(x)
        x = self.sigmoid(x)
        
        return module_input * x

class SEVGG(nn.Module):

    def __init__(self, features, num_classes=1000, init_weights=True):
        super(SEVGG, self).__init__()
        self.features = features
        self.avgpool = nn.AdaptiveAvgPool2d((7, 7))
        self.classifier = nn.Sequential(
            nn.Linear(512 * 7 * 7, 4096),
            nn.ReLU(True),
            nn.Dropout(),
            nn.Linear(4096, 4096),
            nn.ReLU(True),
            nn.Dropout(),
            nn.Linear(4096, num_classes),
        )
        if init_weights:
            self._initialize_weights()

    def forward(self, x):
        x = self.features(x)
        x = self.avgpool(x)
        x = torch.flatten(x, 1)
        x = self.classifier(x)
        
        return x

    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, mode='fan_out', nonlinearity='relu')
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.Linear):
                nn.init.normal_(m.weight, 0, 0.01)
                nn.init.constant_(m.bias, 0)


def make_layers(cfg, batch_norm=False):
    layers = []
    in_channels = 3
    for v in cfg:
        if v == 'M':
            layers += [nn.MaxPool2d(kernel_size=2, stride=2)]
        else:
            conv2d = nn.Conv2d(in_channels, v, kernel_size=3, padding=1)
            if batch_norm:
                layers += [conv2d, nn.BatchNorm2d(v), nn.ReLU(inplace=True)]
            else:
                layers += [conv2d, nn.ReLU(inplace=True)]
            layers += [SEModule(channels=v, reduction=16)]
            in_channels = v
            
    return nn.Sequential(*layers)


cfgs = {
    'A': [64, 'M', 128, 'M', 256, 256, 'M', 512, 512, 'M', 512, 512, 'M'],
    'B': [64, 64, 'M', 128, 128, 'M', 256, 256, 'M', 512, 512, 'M', 512, 512, 'M'],
    'D': [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 'M', 512, 512, 512, 'M', 512, 512, 512, 'M'],
    'E': [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 256, 'M', 512, 512, 512, 512, 'M', 512, 512, 512, 512, 'M'],
}


def _se_vgg(arch, cfg, batch_norm, pretrained, progress, **kwargs):
    # if pretrained:
    #     kwargs['init_weights'] = False
    model = SEVGG(make_layers(cfgs[cfg], batch_norm=batch_norm), **kwargs)
    if pretrained and arch == 'vgg19_bn':
        vgg = models.vgg19_bn(pretrained=True)
        idx = 0
        for i, m in enumerate(model.features):
            if not isinstance(m, SEModule):
                if isinstance(m, nn.Conv2d) or isinstance(m, nn.BatchNorm2d):
                    model.features[i].weight.data.copy_(vgg.features[idx].weight.data)
                    model.features[i].bias.data.copy_(vgg.features[idx].bias.data)
                idx += 1
                
        for i, m in enumerate(model.classifier):
            if isinstance(m, nn.Linear):
                model.classifier[i].weight.data.copy_(vgg.classifier[i].weight.data)
                model.classifier[i].bias.data.copy_(vgg.classifier[i].bias.data)

    return model


